use std::{cell::RefCell, rc::Rc};

/// leet code 算法题解
pub struct Solution;

type TreeNodePtr = Option<Rc<RefCell<TreeNode>>>;
type ListNodePtr = Option<Box<ListNode>>;

/// 树结构
#[derive(Debug, PartialEq, Eq)]
pub struct TreeNode {
    pub val: i32,
    pub left: TreeNodePtr,
    pub right: TreeNodePtr,
}

impl TreeNode {
    #[inline]
    pub fn new(val: i32) -> Self {
        TreeNode { val, left: None, right: None }
    }
}

/// 链表结构
#[derive(PartialEq, Eq, Clone, Debug)]
pub struct ListNode {
    pub val: i32,
    pub next: ListNodePtr,
}

impl ListNode {
    #[inline]
    pub fn new(val: i32) -> Self {
        ListNode { next: None, val }
    }

    #[inline]
    pub fn simplify(val: i32, next: ListNodePtr) -> ListNodePtr {
        Some(Box::new(ListNode { val, next }))
    }
}

/// 创建链表
///
/// # Examples
///
/// ```rust
/// use leet_code::{leet_code::ListNode, linked_list};
///
/// assert_eq!(
///     linked_list!(1, 2, 3),
///     Some(Box::new(ListNode {
///         val: 1,
///         next: Some(Box::new(ListNode {
///             val: 2,
///             next: Some(Box::new(ListNode { val: 3, next: None }))
///         }))
///     }))
/// );
/// ```
#[macro_export]
macro_rules! linked_list {
    () => {
        None
    };
    ($e:expr) => {
        ListNode::simplify($e, None)
    };
    ($e:expr, $($tail:tt)*) => {
        ListNode::simplify($e, linked_list!($($tail)*))
    };
}

/// 创建树
///
/// # Examples
///
/// ```rust
/// use std::{cell::RefCell, rc::Rc};
///
/// use leet_code::leet_code::{linked_tree, TreeNode};
///
/// assert_eq!(
///     linked_tree(1, None, linked_tree(3, None, None)),
///     Some(Rc::new(RefCell::new(TreeNode {
///         val: 1,
///         left: None,
///         right: Some(Rc::new(RefCell::new(TreeNode { val: 3, left: None, right: None })))
///     })))
/// );
/// ```
pub fn linked_tree(val: i32, left: TreeNodePtr, right: TreeNodePtr) -> TreeNodePtr {
    Some(Rc::new(RefCell::new(TreeNode { val, left, right })))
}
